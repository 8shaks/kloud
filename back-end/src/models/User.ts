import mongoose from 'mongoose'
import { IUser } from '../@types/custom';

const Schema = mongoose.Schema;

const UserSchema = new Schema({
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});


const User = mongoose.model<IUser>("users", UserSchema);

export default User